static inline void kllex_discardline(KlLex* lex);
static inline void kllex_discardto(KlLex* lex, char end);


static inline int kllex_nextnonblank(Ki* ki) {
  int ch = ki_getc(ki);
  while (ch == ' ' || ch == '\t' || ch == KOF)
    ch = ki_getc(ki);
  return ch;
}

static inline void kllex_discardline(KlLex* lex) {
  Ki* input = lex->input;
  int ch = ki_getc(input);
  while (ch != '\n' && ch != KOF)
    ch = ki_getc(input);
  ++lex->currline;
}

static inline void kllex_discardto(KlLex* lex, char end) {
  Ki* input = lex->input;
  int ch = ki_getc(input);
  if (ch == '\n') ++lex->currline;
  while (ch != end && ch != KOF) {
    ch = ki_getc(input);
    if (ch == '\n') ++lex->currline;
  }
}

static inline int kllex_nextvalid(KlLex* lex) {
  Ki* input = lex->input;
  while (true) {
    int ch = kllex_nextnonblank(input);
    if (ch == '\n') {       /* is new line */
      ++lex->currline;
    } else if (ch == '#') { /* is comment */
      kllex_discardline(lex);
    } else {                /* else is valid */
      return ch;
    }
  }
}

bool kllex_init(KlLex* lex, Ki* ki, Ko* err, const char* inputname) {
  lex->input = ki;
  lex->err = err;
  lex->nerror = 0;
  char* namebuf = (char*)malloc((strlen(inputname) + 1) * sizeof (char));
  if (kl_unlikely(!namebuf)) return false;
  strcpy(namebuf, inputname);
  lex->inputname = namebuf;
  lex->currline = 1;
  return true;
}

void kllexer_destroy(KlLex* lex) {
  free(lex->inputname);
}

KlLex* kllex_create(Ki* ki, Ko* err, const char* inputname) {
  KlLex* lex = (KlLex*)malloc(sizeof (KlLex));
  if (kl_unlikely(!lex)) return NULL;
  if (kl_unlikely(!kllex_init(lex, ki, err, inputname))) {
    free(lex);
    return NULL;
  }
  return lex;
}

void kllex_delete(KlLex* lex) {
  kllex_destroy(lex);
  free(lex);
}

static void kllex_escapestring(KlLex* lex) {
  char* p = lex->buf;
  char* pend = lex->buf + KLLEX_BUFSIZE - 1;
  Ki* input = lex->input;
  while (p != pend) {
    int ch = ki_getc(input);
    if (ch == '\\') { /* is escape character */
      switch ((ch = ki_getc(input))) {
        case 'n': *p++ = '\n'; break;
        case 't': *p++ = '\t'; break;
        case 'a': *p++ = '\a'; break;
        case 'b': *p++ = '\b'; break;
        case 'r': *p++ = '\r'; break;
        case 'f': *p++ = '\f'; break;
        case 'v': *p++ = '\v'; break;
        case '\\': *p++ = '\\'; break;
        case '"': *p++ = '"'; break;
        case 'x': {   /* \xAB */
          ch = ki_getc(input);  /* get A */
          unsigned char hex = 0;
          if ((ch >= '0' && ch <= '9') || (ch >= 'a' && ch <= 'f') || (ch >= 'A' && ch <= 'F')) {
            hex = ch <= '9' ? (ch - '0') * 16 : (kl_tolower(ch) - 'a' + 10) * 16;
          } else {
            if (ch == '\n') ++lex->currline;
            kllex_error(lex, "error: unexpected character in a string");
            kllex_discardto(lex, '"');
            *p = '\0';
            return;
          }
          ch = ki_getc(input);  /* get B */
          if ((ch >= '0' && ch <= '9') || (ch >= 'a' && ch <= 'f') || (ch >= 'A' && ch <= 'F')) {
            hex = ch <= '9' ? ch - '0': (kl_tolower(ch) - 'a' + 10);
          } else {
            if (ch == '\n') ++lex->currline;
            kllex_error(lex, "error: unexpected character in a string");
            kllex_discardto(lex, '"');
            *p = '\0';
            return;
          }
          *p++ = hex;
        }
      }
    } else if (ch == '\n') {
      ++lex->currline;
      kllex_error(lex, "error: unexpected character in a string");
      kllex_discardto(lex, '"');
      *p = '\0';
      return;
    } else if (ch != '"') {
      *p++ = ch;
    } else {
      *p = '\0';
      return;
    }
  }
  *p = '\0';
  if (ki_getc(input) != '"') {
    kllex_error(lex, "error: string too long");
    kllex_discardto(lex, '"');
    return;
  }
}

static void kllex_handlestring(KlLex* lex, int ch) {
  lex->tok.kind = KLTK_STRING;
  lex->tok.string = lex->buf;
  if (ch == '`') {    /* non-escape string */
    char* p = lex->buf;
    char* pend = lex->buf + KLLEX_BUFSIZE - 1;
    Ki* input = lex->input;
    ch = ki_getc(input);
    if (ch == '\n') ++lex->currline;
    while (p != pend && ch != '`' && ch != KOF) {
      *p++ = ch;
      ch = ki_getc(input);
      if (ch == '\n') ++lex->currline;
    }
    *p = '\0';
    if (ch != '`') {
      kllex_error(lex, "error: string too long");
      kllex_discardto(lex, '`');
    }
  } else {
    kl_assert(ch == '\"', "something wrong in lexer when reading a string");

    kllex_escapestring(lex);
  }
}

void kllex_error(KlLex* lex, const char* format, ...) {
  ++lex->nerror;
  va_list arglist;
  va_start(arglist, format);
  kllex_show_info(lex, format, arglist);
  va_end(arglist);
}

void kllex_show_info(KlLex* lex, const char* format, va_list vlst) {
  size_t col = 0; /* TODO: get column */
  ko_printf(lex->err, "%s:%zu:%zu:", lex->inputname, lex->currline, col);
  ko_vprintf(lex->err, format, vlst);
  /* TODO: mark error position */
}


#define kllex_return  lex->tok.end.line = lex->currline;    \
                      lex->tok.end.offset = ki_tell(input); \
                      return

void kllex_next(KlLex* lex) {
  Ki* input = lex->input;

  int ch = kllex_nextvalid(lex);
  lex->tok.begin.line = lex->currline;
  lex->tok.begin.offset = ki_tell(input);
  if (ch == KOF) {
    lex->tok.kind = KLTK_END;
    kllex_return;
  }
  uint8_t state = start;
  uint8_t nextstate;
  int buflen = 0;
  while (ch != KOF && (nextstate = transition[state][(uint8_t)ch]) != KLLEX_DEADSTATE) {
    state = nextstate;
    lex->buf[buflen++] = ch;
    ch = ki_getc(input);
  }
  if (ch != KOF) ki_ungetc(input);
  switch (tokenkind[ch]) {
    case KLTK_STRING: {
      kllex_handlestring(lex, lex->buf[0]);
      kllex_return;
    }
    case KLTK_INT: {
      /* TODO: handle integer */
    }
    case KLTK_BOOLVAL: {
      lex->tok.kind = KLTK_BOOLVAL;
      lex->tok.boolval = lex->buf[0] == 't' ? KL_TRUE : KL_FALSE;
      kllex_return;
    }
    default: {
    }
  }
}

