/* exclude '\n' */
static inline int kllex_nextnonblank(Ki* ki);
/* read next character.
 * discard leading whilespaces('\t', '\n', ' ') and comments. */
static inline int kllex_nextvalid(KlLex* lex);
static inline void kllex_discardline(KlLex* lex);
static inline void kllex_discardto(KlLex* lex, char end);

static inline char kllex_finishnl(KlLex* lex, Ki* input) {
  int ch = ki_getc(input);
  if (ch != '\r' && ch != KOF) ki_ungetc(input);
  ++lex->currline;
  return '\n';
}

static inline int kllex_readmaybenl(KlLex* lex, Ki* input) {
  int ch = ki_getc(input);
  return kl_isnl(ch) ? kllex_finishnl(lex, input) : ch;
}

static inline int kllex_nextnonblank(Ki* ki) {
  int ch = ki_getc(ki);
  while (ch == ' ' || ch == '\t')
    ch = ki_getc(ki);
  return ch;
}

static inline void kllex_discardline(KlLex* lex) {
  Ki* input = lex->input;
  int ch = ki_getc(input);
  while (!kl_isnl(ch) && ch != KOF)
    ch = ki_getc(input);
  kllex_finishnl(lex, input);
}

static inline void kllex_discardto(KlLex* lex, char end) {
  Ki* input = lex->input;
  int ch;
  do {
    ch = kllex_readmaybenl(lex, input);
  } while (ch != end && ch != KOF);
}

static inline int kllex_nextvalid(KlLex* lex) {
  Ki* input = lex->input;
  while (true) {
    int ch = kllex_nextnonblank(input);
    if (kl_isnl(ch)) {      /* is new line */
      kllex_finishnl(lex, input);
    } else if (ch == '#') { /* is comment */
      kllex_discardline(lex);
    } else {                /* else is valid */
      return ch;
    }
  }
}

bool kllex_init(KlLex* lex, Ki* ki, Ko* err, const char* inputname, KlStrTab* strtab) {
  lex->input = ki;
  lex->err = err;
  lex->nerror = 0;
  char* namebuf = (char*)malloc((strlen(inputname) + 1) * sizeof (char));
  if (kl_unlikely(!namebuf)) return false;
  strcpy(namebuf, inputname);
  lex->inputname = namebuf;
  lex->currline = 1;
  lex->strtab = strtab;
  return true;
}

void kllex_destroy(KlLex* lex) {
  free(lex->inputname);
}

KlLex* kllex_create(Ki* ki, Ko* err, const char* inputname, KlStrTab* strtab) {
  KlLex* lex = (KlLex*)malloc(sizeof (KlLex));
  if (kl_unlikely(!lex)) return NULL;
  if (kl_unlikely(!kllex_init(lex, ki, err, inputname, strtab))) {
    free(lex);
    return NULL;
  }
  return lex;
}

void kllex_delete(KlLex* lex) {
  kllex_destroy(lex);
  free(lex);
}

static size_t kllex_escapestring(KlLex* lex, char* buf) {
  char* p = buf;
  char* pend = buf + KLLEX_STRLIMIT;
  Ki* input = lex->input;
  while (p != pend) {
    int ch = ki_getc(input);
    if (ch == '\\') { /* is escape character */
      switch ((ch = ki_getc(input))) {
        case 'n': *p++ = '\n'; break;
        case 't': *p++ = '\t'; break;
        case 'a': *p++ = '\a'; break;
        case 'b': *p++ = '\b'; break;
        case 'r': *p++ = '\r'; break;
        case 'f': *p++ = '\f'; break;
        case 'v': *p++ = '\v'; break;
        case '\\': *p++ = '\\'; break;
        case '\'': *p++ = '\''; break;
        case '"': *p++ = '"'; break;
        case 'x': {   /* \xAB */
          ch = ki_getc(input);  /* get A */
          unsigned char hex = 0;
          if ((ch >= '0' && ch <= '9') || (ch >= 'a' && ch <= 'f') || (ch >= 'A' && ch <= 'F')) {
            hex = ch <= '9' ? (ch - '0') * 16 : (kl_tolower(ch) - 'a' + 10) * 16;
          } else {
            if (kl_isnl(ch)) kllex_finishnl(lex, input);
            kllex_error(lex, "error: unexpected character in a string");
            kllex_discardto(lex, '"');
            return 0;
          }
          ch = ki_getc(input);  /* get B */
          if ((ch >= '0' && ch <= '9') || (ch >= 'a' && ch <= 'f') || (ch >= 'A' && ch <= 'F')) {
            hex = ch <= '9' ? ch - '0': (kl_tolower(ch) - 'a' + 10);
          } else {
            if (kl_isnl(ch)) kllex_finishnl(lex, input);
            kllex_error(lex, "error: unexpected character in a string");
            kllex_discardto(lex, '"');
            return 0;
          }
          *p++ = hex;
        }
      }
    } else if (kl_isnl(ch)) {
      kllex_finishnl(lex, input);
      kllex_error(lex, "error: unexpected character in a string");
      kllex_discardto(lex, '"');
      return 0;
    } else if (ch != '"') { /* normal character */
      *p++ = ch;
    } else {                /* end of string */
      return p - buf;
    }
  }
  if (ki_getc(input) != '"') {
    kllex_error(lex, "error: string too long");
    kllex_discardto(lex, '"');
    return 0;
  }
  return p - buf;
}

static size_t kllex_longstring(KlLex* lex, char* buf) {
  char* p = buf;
  char* pend = buf + KLLEX_STRLIMIT;
  Ki* input = lex->input;
  int ch = kllex_readmaybenl(lex, input);
  while (p != pend && ch != '`' && ch != KOF) {
    *p++ = ch;
    ch = kllex_readmaybenl(lex, input);
  }
  if (ch == KOF) {
    kllex_error(lex, "error: missing '`'");
    return 0;
  } else if (ch != '`') {
    kllex_error(lex, "error: string too long");
    kllex_discardto(lex, '`');
    return 0;
  }
  return p - buf;
}

static void kllex_handlestring(KlLex* lex, char* buf, int ch) {
  kl_assert(ch == '\"' || ch == '`', "something wrong in lexer when reading a string");
  lex->tok.string.id = klstrtab_stringid(lex->strtab, buf);
  size_t len = ch == '`' ? kllex_longstring(lex, buf) : kllex_escapestring(lex, buf);
  klstrtab_pushstring(lex->strtab, len);
  lex->tok.string.length = len;
}

static inline void kllex_handleinteger(KlLex* lex, char* buf, size_t len) {
  if (len == KLLEX_STRLIMIT) {  /* too long */
    /* we can't use strtoll in this case because there is no space for '\0'. */
    kllex_error(lex, "number too long");
    len = KLLEX_STRLIMIT - 1;
  }
  buf[len] = '\0';
  int base = buf[0] != '0' || len == 1 ? 10 : buf[0] == '0' ? 8 : 16;
  lex->tok.intval = strtoll(buf, NULL, base);
}

static size_t kllex_escapeid(KlLex* lex, char* buf) {
  char* p = buf;
  char* pend = buf + KLLEX_STRLIMIT;
  Ki* input = lex->input;
  while (p != pend) {
    int ch = ki_getc(input);
    if (ch == '\\') { /* is escape character */
      switch ((ch = ki_getc(input))) {
        case 'n': *p++ = '\n'; break;
        case 't': *p++ = '\t'; break;
        case 'a': *p++ = '\a'; break;
        case 'b': *p++ = '\b'; break;
        case 'r': *p++ = '\r'; break;
        case 'f': *p++ = '\f'; break;
        case 'v': *p++ = '\v'; break;
        case '\\': *p++ = '\\'; break;
        case '\'': *p++ = '\''; break;
        case '"': *p++ = '"'; break;
        default: *p++ = ch; break;
      }
    } else if (kl_isnl(ch)) {
      kllex_finishnl(lex, input);
      kllex_error(lex, "error: unexpected character in an identifier");
      kllex_discardto(lex, '\'');
      return 0;
    } else if (ch != '\'') { /* normal character */
      *p++ = ch;
    } else {                /* end of string */
      return p - buf;
    }
  }
  if (ki_getc(input) != '\'') {
    kllex_error(lex, "error: identifier too long");
    kllex_discardto(lex, '\'');
    return 0;
  }
  return p - buf;
}

void kllex_error(KlLex* lex, const char* format, ...) {
  ++lex->nerror;
  va_list arglist;
  va_start(arglist, format);
  kllex_show_info(lex, format, arglist);
  va_end(arglist);
}

void kllex_show_info(KlLex* lex, const char* format, va_list vlst) {
  size_t col = 0; /* TODO: get column */
  ko_printf(lex->err, "%s:%zu:%zu:", lex->inputname, lex->currline, col);
  ko_vprintf(lex->err, format, vlst);
  /* TODO: mark error position */
  ko_flush(lex->err);
}


#define kllex_return(tokkind) {         \
  lex->tok.end.line = lex->currline;    \
  lex->tok.end.offset = ki_tell(input); \
  lex->tok.kind = (tokkind);            \
  return;                               \
}

void kllex_next(KlLex* lex) {
  Ki* input = lex->input;

  int ch = kllex_nextvalid(lex);
  lex->tok.begin.line = lex->currline;
  lex->tok.begin.offset = ki_tell(input);
  if (ch == KOF) kllex_return(KLTK_END);

  uint8_t state = start;
  uint8_t nextstate;
  char* buf = klstrtab_allocstring(lex->strtab);
  if (kl_unlikely(!buf)) {
    kllex_error(lex, "out of memory in kllex_next(). pretend to reach end of file");
    kllex_return(KLTK_END);
  }
  size_t strlength = 0;
  while (ch != KOF && (nextstate = transition[state][(uint8_t)ch]) != KLLEX_DEADSTATE && strlength < KLLEX_STRLIMIT) {
    state = nextstate;
    buf[strlength++] = ch;
    ch = ki_getc(input);
  }
  if (ch != KOF) ki_ungetc(input);
  KlToken kind = tokenkind[state];
  switch (kind) {
    case KLTK_STRING: {
      kllex_handlestring(lex, buf, buf[0]);
      kllex_return(KLTK_STRING);
    }
    case KLTK_INT: {
      kllex_handleinteger(lex, buf, strlength);
      kllex_return(KLTK_INT);
    }
    case KLTK_ID: {
      size_t len = buf[0] == '\'' ? kllex_escapeid(lex, buf) : strlength;
      lex->tok.string.length = len;
      klstrtab_pushstring(lex->strtab, len);
      kllex_return(KLTK_ID);
    }
    case KLTK_BOOLVAL: {
      lex->tok.boolval = buf[0] == 't' ? KL_TRUE : KL_FALSE;
      kllex_return(KLTK_BOOLVAL);
    }
    case KLTK_ERR: {
      kllex_error(lex, "unexpected character");
      /* do recursion */
      kllex_next(lex);
      return;
    }
    default: {
      kllex_return(kind);
    }
  }
}



